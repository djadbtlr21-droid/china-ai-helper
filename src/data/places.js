export function getPlaceById(id) {
  return places.find(p => p.id === Number(id));
}

export function filterPlaces({ category, subcategory, query }) {
  return places.filter(p => {
    if (category && p.category !== category) return false;
    if (subcategory && subcategory !== '전체') {
      if (p.subCategory !== subcategory) return false;
    }
    if (query) {
      const q = query.toLowerCase();
      return p.nameKo?.toLowerCase().includes(q)
        || p.nameZh?.toLowerCase().includes(q)
        || p.addressKo?.toLowerCase().includes(q)
        || p.description?.toLowerCase().includes(q)
        || p.tags?.some(t => t.includes(q));
    }
    return true;
  });
}
